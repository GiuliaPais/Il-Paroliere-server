package uninsubria.server.room.game;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Starting game state.
     */
    STARTING,
    /**
     * Ongoing game state.
     */
    ONGOING,
    /**
     * Finished game state.
     */
    FINISHED,
    /**
     * Interrupted game state.
     */
    INTERRUPTED
}
